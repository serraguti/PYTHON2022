print("Ejemplo condicional estaciones")
print("Introduzca número (1-4)")
numero = int(input())
if (numero == 1):
    print("INVIERNO")
elif (numero == 2):
    print("VERANO")
elif (numero == 3):
    print("OTOÑO")
elif (numero == 4):
    print("PRIMAVERA")
else:
    print("Opción incorrecta")
print("Fin de programa")